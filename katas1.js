function oneThroughTwenty() { 
  for(let i = 1; i <= 20; i++){
    console.log(i)
  }   
}

//call function oneThroughTwenty

function evensToTwenty() {  
  for(let i = 2; i <= 20 ; i += 2){
    console.log(i)
  }
}

//call function evensToTwenty

